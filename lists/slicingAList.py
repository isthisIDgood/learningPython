#Slicing Lists

names = [
    'Brent',
    'Trent',
    'Jael',
    'Richard'
]
print ("names[0:2]:")
print ( names[0:2] )

print ( '\nnames[1:3]:' )
print ( names[1:3] )

print( '\nnames[:2]:')
print( names[:2] )

print( '\nnames [3:]:')
print( names[3:] )

print( '\nnames[-3:]:' )
print( names[-3:] ) 